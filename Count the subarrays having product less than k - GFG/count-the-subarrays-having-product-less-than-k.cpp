class Solution{
  public:
    int countSubArrayProductLessThanK(const vector<int>& a, int n, long long k) {
        if(k==1) return 0;
        long long mul = 1 ;
        int ans =0 , i =0 , j =0 ;
        while(i<n){
            mul *= a[i];
            while(j<=i&&mul>=k){
                mul /= a[j];
                j++;
            }
            ans += (i-j+1);
            i++;
        }
        return ans;
    }
};

